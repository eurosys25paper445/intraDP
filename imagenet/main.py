class AverageMeter(object):
    """Computes and stores the average and current value"""

    def __init__(self):
        self.reset()

    def reset(self):
        self.val = 0
        self.avg = 0
        self.sum = 0
        self.count = 0

    def update(self, val, n=1):
        self.val = val
        self.sum += val * n
        self.count += n
        self.avg = self.sum / self.count


def main():
    args = parser.parse_args()

    print(f"{args.arch} Using device: {device}")
    model = models.__dict__[args.arch]()
    model.eval()
    model = model.to(device)

    PCI = ParallelCollaborativeInference(parallel_approach="select",ip="192.168.50.11")
    PCI.start_client(model=model)

    valdir = args.data
    normalize = transforms.Normalize(mean=[0.485, 0.456, 0.406],
                                     std=[0.229, 0.224, 0.225])

    val_loader = torch.utils.data.DataLoader(
        datasets.ImageFolder(valdir, transforms.Compose([
            transforms.Resize(256),
            transforms.CenterCrop(224),
            transforms.ToTensor(),
            normalize,
        ])),
        batch_size=args.batch_size, shuffle=False,
        num_workers=args.workers, pin_memory=True)


    inference_time=AverageMeter()

    model.eval()
    tag = 0
    with torch.no_grad():
        for i, (input, target) in enumerate(val_loader):
            input = input.to(device)
            torch.cuda.synchronize()
            start = time.time()
            output = model(input)
            torch.cuda.synchronize()
            end = time.time()
            inference_time.update((end-start)*1000)
            # print(f"result is {output}")
            print(f"inference time: {inference_time.val:.3f} ms, average {inference_time.avg:.3f} ms")
            if tag == 10:
                break
            tag = tag + 1
           
if __name__ == '__main__':
    main()
